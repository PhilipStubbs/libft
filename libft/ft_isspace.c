#include "../includes/libft.h"

int	ft_isspace(char c)
{
	if ((c == '\n') || (c == '\t') || (c == '\v') ||
		(c == ' ') || (c == '\f') || (c == '\r'))
		return (1);
	else
		return (0);
}
