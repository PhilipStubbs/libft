#include "../includes/libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483647 - 1)
		write(1, "-2147483648\0", 12);
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
	}
	ft_putchar((n % 10) + '0');
}
